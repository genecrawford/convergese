                    <h2>Generational Misfits - Designing for the next Generation.</h2>

<p>Objective: Move over Xennials and Millennials, the Plurals are here. Gen Z, the post-millennials, and they are the largest and most diverse cohort in U.S. History.  Their normal is the old unconventional and we have to adapt how we design to truly engage this tech-savvy bunch.</p>

<p>I remember the first time an AOL CD-ROM appeared in my parent’s mailbox. It promised me thousands of minutes to connect with others through our computer. A computer, that up until that moment, had been used primarily for solitaire.</p>

<p>I patiently waited for the program to load. Nothing happened. Where was my Internet? I didn’t realize I needed a phone line to connect. I “borrowed” a phone cord from my parents room and figured out how to connect the computer’s modem to the phone jack. For the first time, I heard the strange sound of dial up, and the word “Connected!” appeared. I was online.</p>

<p>Learning to use the web has changed slightly since then. While everything in the past had to be self-taught, we can now get degrees or go to bootcamps to learn all kinds of Internet technology. Indeed, staying abreast of the latest techniques is a must for designers and developers to do their jobs. As part of that education, we need to stay focused on trends within new Generations. GenZ is the first generation who are truly digital natives. They make up 25% of the population, representing how future technology users will navigate the web and expect applications and interfaces to work. By paying attention to how GenZ uses technology, we can both improve the quality of our own work and make future technology more accessible and useful going forward.</p>

